package norman.gurps.create.model.response;

public class OtherAttributes {
    private Integer encumbranceLevel;
    private Integer encumberedMove;
    private Integer damageResistance;
    private Integer dodge;
    private Integer frightCheck;
    private Integer mentalStunCheck;
    private Integer physicalStunCheck;
    private Integer deathCheck;

    public Integer getEncumbranceLevel() {
        return encumbranceLevel;
    }

    public void setEncumbranceLevel(Integer encumbranceLevel) {
        this.encumbranceLevel = encumbranceLevel;
    }

    public Integer getEncumberedMove() {
        return encumberedMove;
    }

    public void setEncumberedMove(Integer encumberedMove) {
        this.encumberedMove = encumberedMove;
    }

    public Integer getDamageResistance() {
        return damageResistance;
    }

    public void setDamageResistance(Integer damageResistance) {
        this.damageResistance = damageResistance;
    }

    public Integer getDodge() {
        return dodge;
    }

    public void setDodge(Integer dodge) {
        this.dodge = dodge;
    }

    public Integer getFrightCheck() {
        return frightCheck;
    }

    public void setFrightCheck(Integer frightCheck) {
        this.frightCheck = frightCheck;
    }

    public Integer getMentalStunCheck() {
        return mentalStunCheck;
    }

    public void setMentalStunCheck(Integer mentalStunCheck) {
        this.mentalStunCheck = mentalStunCheck;
    }

    public Integer getPhysicalStunCheck() {
        return physicalStunCheck;
    }

    public void setPhysicalStunCheck(Integer physicalStunCheck) {
        this.physicalStunCheck = physicalStunCheck;
    }

    public Integer getDeathCheck() {
        return deathCheck;
    }

    public void setDeathCheck(Integer deathCheck) {
        this.deathCheck = deathCheck;
    }
}
